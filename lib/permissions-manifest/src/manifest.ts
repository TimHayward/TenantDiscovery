export const metricDataSources = Object.values(metricDataSourceRegistry) as MetricDataSourceEntry[];

export type PermissionName = (typeof permissionsManifest.permissions)[number]["name"];
export type FeatureId = (typeof permissionsManifest.features)[number]["id"];

export const permissionsByName = new Map(
  permissionsManifest.permissions.map((permission) => [permission.name, permission])
);

export const featuresById = new Map(
  permissionsManifest.features.map((feature) => [feature.id, feature])
);

export const metricDataSourcesById = new Map(
  metricDataSources.map((metric) => [metric.metricId, metric])
);

export function getPermissionDefinition(name: PermissionName | string) {
  return permissionsByName.get(name);
}

export function getFeatureDefinition(id: FeatureId | string) {
  return featuresById.get(id);
}

export function getPermissionsByTier(tier: PermissionTier) {
  return permissionsManifest.permissions.filter((permission) => permission.tier === tier);
}

export function getMetricDataSourceEntry(metricId: string) {
  return metricDataSourcesById.get(metricId);
}

export function getManualCheckDefinition(metricId: string) {
  return metricDataSourcesById.get(metricId)?.manualCheck;
}

export function getMetricDataSourcesByTab(tab: string) {
  return metricDataSources.filter((metric) => metric.tab === tab);
}

export function getFeaturePermissionDetails(id: FeatureId | string) {
  const feature = getFeatureDefinition(id);
  if (!feature) return null;

  const resolvePermissions = (names: readonly string[]) =>
    names.flatMap((name) => {
      const permission = permissionsByName.get(name);
      return permission ? [permission] : [];
    });

  return {
    ...feature,
    requiredPermissions: resolvePermissions(feature.requiredPermissions),
    optionalPermissions: resolvePermissions(feature.optionalPermissions),
    futurePermissions: resolvePermissions(feature.futurePermissions),
  };
}

export function getPermissionNamesForFeature(
  id: FeatureId | string,
  tier: PermissionTier
) {
  const details = getFeaturePermissionDetails(id);
  if (!details) return [];
  if (tier === "required") return details.requiredPermissions.map((permission) => permission.name);
  if (tier === "optional") return details.optionalPermissions.map((permission) => permission.name);
  return details.futurePermissions.map((permission) => permission.name);
}